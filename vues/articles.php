        <h2>Les musiques indémodables.</h2>
        <div id="content">
            <img src="./assets/img/fete.jpg" alt="image article" id="imgleft">
            <div id="chapo">
                La musique existe depuis les temps les plus reculés, avant même les premières traces humaines. Il est
                impossible de dater avec exactitude les débuts de la musique. En revanche, ce que nous savons c’est que
                depuis qu’elle existe elle fait partie intégrante de nos vies. En effet, déjà autrefois elle aidait à
                traduire des sentiments, des émotions élémentaires et le rythme leur donnait vie. Aujourd’hui, nous
                l’écoutons en toutes circonstances : dans les instants d'intimité (notre premier baiser, par exemple),
                dans les moments de partage (mariages, baptêmes, fêtes entre amis). Cependant, les musiques que nous
                écoutons le plus ne sont pas celles qui rythment notre quotidien d’adulte actif mais celles qui ont
                bercé notre enfance et notre adolescence.
            </div>
            <div id="contentright">
                Par quels moyens ces musiques ont-elles réussi à traverser les générations sans vraiment se démoder ?
            </div>

            <div id="contentleft">
                Loin d'illustrer un conflit de générations ou une potentielle déchéance de l'industrie musicale, ce
                phénomène aurait une explication tout à fait scientifique. En effet, écouter nos chansons préférées
                augmenterait la production d’hormones dites “du plaisir” - comme la dopamine, la sérotonine ou
                l’ocytocine. Et durant notre puberté, ce phénomène est amplifié puisqu’il s'agit d’une période de notre
                vie où notre système hormonal se met en place et où la création  d'hormones est plus importante.
                Ces dernières années, psychologues et experts en neurosciences ont confirmé que ces chansons exerçaient
                un pouvoir disproportionné sur nos émotions. Et ces chercheurs ont découvert des preuves démontrant que
                notre cerveau crée un lien plus étroit avec la musique que nous écoutions adolescents qu’avec tout ce
                que nous pouvons entendre une fois adulte.
                En fait, durant notre adolescence, voire notre enfance, nous avons écouté des musiques que nous avons
                associées à des moments de bonheur ; ces musiques garderont à nos yeux une place très particulière car
                elles nous ont aidés à tisser des liens sociaux. Ce sont des années que nous qualifions de “formatrices”.
                À l’époque, nous préférions jouer avec des amis ou passer du temps en famille. Durant tous ces moments,
                la musique ajoutait une ambiance de tranquillité, de sérénité, de calme, et conférait un certain
                sentiment de sécurité, ce que nous avons du mal à ressentir lorsque nous entrons dans la vie active.
                Stress, pression extérieure, etc.... notre environnement n’est plus autant propice au développement.
                De plus, les musiques actuelles ne nous touchent plus autant que celles de notre passé. Nous sommes
                toujours capables d'apprécier de nouveaux artistes, de garder une chanson actuelle en tête pendant
                plusieurs heures, mais seule une “vieille”chanson pourra nous rendre nostalgique, nous faire sourire
                et nous procurer un sentiment de bien-être incomparable.

            </div>
