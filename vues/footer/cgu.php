<h3>Conditions Générales d'Utilisation</h3><br />

Libwav étant un projet tutoré de deuxième année de DUT MMI, les données que nous récoltons ne servent que dans un but de renseignements. En effet, nous aimerions savoir quel type de population notre site a touché suite à sa mise en ligne.
Vos données ne sont donc ni vendues ni redistribuées mais juste sauvegardées dans nos bases de données afin d’être évaluées.
